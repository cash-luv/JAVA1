package day1;

public class 타입_블리언 {

	public static void main(String[] args) {
		// 두가지 값만 출력 가능
		boolean a = true; // 참
		boolean b = false; // 거짓 
		
		System.out.println(a);
		System.out.println(b);
		
		int c = 10;
		int d = 4;
		boolean e = c > d;
		boolean f = c == d;
		
		System.out.println(e);
		System.out.println(f);
		

	}

}
